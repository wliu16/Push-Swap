int	min_2(int a, int b)
{
	if (a <= b)
		return (a);
	else
		return (b);
}

int	max_2(int a, int b)
{
	if (a >= b)
		return (a);
	else
		return (b);
}

int	abs(int x)
{
	if (x < 0)
		return (-1 * x);
	else
		return (x);
}
